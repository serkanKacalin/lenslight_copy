import mongoose from "mongoose";

const { Schema } = mongoose;

const userSchema = new Schema(
{
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true 
    },
    password: {
        type: String,
        required: true
    },
},
{
    timestamps: true, // mongoose veri taban覺nda bizim ad覺m覺za created at ve updated at ad覺nda iki alan ekler.
}
);

const User = mongoose.model("User", userSchema); 

export default User